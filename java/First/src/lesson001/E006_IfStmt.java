package lesson001;

public class E006_IfStmt {

	public static void main(String[] args) {
		// If statement : 조건이 만족할 때 코드블록을 실행한다
		//                만족하지 않으면 코드블
		// If Syntax
		// if(조건) {...}
		int age = 17;
		if( age >=20) {
            System.out.println("성년");
        }
        System.out.println("프로그램 종료...");
	}

}
